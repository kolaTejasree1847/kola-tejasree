package Array;

public class swapping<Type> {
   public void swap(Type a, Type b) {
	   System.out.println("before swapping");
	   System.out.println("a="+a+"\nb="+b+"\n\n");
	   Type x=a;
	   a=b;
	   b=x;
	   System.out.println("after swapping");
	   System.out.println("a="+a+"\nb="+b);
   }
}






